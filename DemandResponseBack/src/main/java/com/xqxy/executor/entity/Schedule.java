package com.xqxy.executor.entity;

import lombok.Data;

import java.math.BigDecimal;

/**
 * @Description 需求响应计划表
 * @Author Rabbit
 * @Date 2022/7/5 10:34
 */
@Data
public class Schedule {
    /**
     * 省/市编码
     */
    String provinceCode;
    /**
     * 事件ID
     */
    String eventId;
    /**
     * 事件名称
     */
    String eventName;
    /**
     * 父节点编码
     */
    String parentCode;
    /**
     * 执行开始时间
     */
    String executionStart;
    /**
     * 执行结束时间
     */
    String endOfExecution;
    /**
     * 调控目标（万kW）
     */
    BigDecimal regulation;
}
