package com.xqxy.dr.modular.anhui.enums;

import lombok.Getter;
/**
 * <p>
 * 合肥营销档案客户状态枚举类
 * </p>
 *
 * @author liuyu
 * @since 2021-12-01
 */
@Getter
public enum CustStatusEnum {

    CUST_STATUS_NORMAL("01", "正常"),

    CUST_STATUS_DISABLED("02", "停用"),

    CUST_STATUS_CANCEL("03", "注销");

    private final String code;

    private final String message;

    CustStatusEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }
}
