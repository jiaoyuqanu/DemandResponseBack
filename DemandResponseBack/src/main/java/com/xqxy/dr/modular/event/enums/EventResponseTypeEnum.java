package com.xqxy.dr.modular.event.enums;

import lombok.Getter;

/**
 * 响应事件类型
 */
@Getter
public enum EventResponseTypeEnum {

    PEAK_OF_INVITATION("1", "邀约削峰"),
    FILL_OF_INVITATION("2", "邀约填谷"),
    PEAK_OF_REALTIME("3", "实时削峰"),
    FILL_OF_REALTIME("4", "实时填谷");

    private final String code;

    private final String message;

    EventResponseTypeEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }
}
