package com.xqxy.dr.modular.event.enums;

import lombok.Getter;

@Getter
public enum BaselineStatusEnum {

    NOT_CAL("1", "未计算"),

    CAL_FINISHED("2", "计算完成"),

    ERROR_CAL("3", "异常");

    private final String code;

    private final String message;

    BaselineStatusEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }
}
