package com.xqxy.dr.modular.project.enums;

import lombok.Getter;

/**
 * 项目状态枚举
 */
@Getter
public enum ProjectStatusEnums {

    NEW("1", "新建"),

    PUBLICITY("2", "公示"),

    IN_EXECUTION("3", "执行中"),

    FINISHED("3", "已结束");

    private final String code;

    private final String message;

    ProjectStatusEnums(String code, String message) {
        this.code = code;
        this.message = message;
    }
}
