package com.xqxy.sys.modular.sms.enums;

import lombok.Getter;

/**
 * 短信模板是否生效
 */
@Getter
public enum SmsTemplateValidEnum {

    IS_VALID("Y","有效"),
    NOT_VALID("N","无效");

    private final String code;

    private final String message;

    SmsTemplateValidEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }
}
